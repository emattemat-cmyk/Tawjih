@app.route("/", methods=["GET", "POST"])
def index():
    if request.method == "POST":
        subjects = ["???????","??????","???? ??????","????","?????","?????"]
        scores = {}
        for s in subjects:
            scores[s] = float(request.form.get(s,0))
        
        results = {}
        for filiere, sub_list in filiere_subjects.items():
            total = sum([scores.get(sub,0) for sub in sub_list])
            max_total = len(sub_list)*20
            pourcentage = round((total/max_total)*100,2)
            score = round((total/len(sub_list)),2)
            results[filiere] = {"pourcentage": pourcentage, "score": score}

        sorted_results = dict(sorted(results.items(), key=lambda x: x[1]["pourcentage"], reverse=True))
        best_filiere = list(sorted_results.keys())[0]

        return render_template("result.html", results=sorted_results, best_filiere=best_filiere)
    
